<template>
    <div class="bs_wrapper">
        <div class="bs_wrapper-cont">
            <ul class="bs_cont">
                <li class="bs_cont-item"
                    :key="index"
                    v-for="(item, index) in (itemData || [])"
                    v-text="item">
                </li>
            </ul>
        </div>
    </div>
</template>

<script>
// import scrollFactory from '../commons/betterScroll';

export default {
    name: 'bs-list',
    data () {
        return {
            itemData: []
        };
    },
    mounted() {
        const _that = this;
        _that.genItemData();
        // setTimeout(() => {
        //     let bScroll = scrollFactory(_that.$el);
        // });
    },
    methods: {
        genItemData (lower = 0, upper = 100) {
            const _that = this;
            for (let i = lower; i < upper; i++) {
                _that.itemData.push(i);
            }
        }
    }
};
</script>

